package haudq.dtos;

import java.io.Serializable;

/**
 *
 * @author PauL
 */
public class UserErrorDTO implements Serializable{
    private String usernameError, passwordError, fullnameError, roleError, confirmPassError, roomIDError;

    public String getRoomIDError() {
        return roomIDError;
    }

    public void setRoomIDError(String roomIDError) {
        this.roomIDError = roomIDError;
    }

    public String getConfirmPassError() {
        return confirmPassError;
    }

    public void setConfirmPassError(String confirmPassError) {
        this.confirmPassError = confirmPassError;
    }

    public UserErrorDTO() {
    }

    public String getUsernameError() {
        return usernameError;
    }

    public void setUsernameError(String usernameError) {
        this.usernameError = usernameError;
    }

    public String getPasswordError() {
        return passwordError;
    }

    public void setPasswordError(String passwordError) {
        this.passwordError = passwordError;
    }

    public String getFullnameError() {
        return fullnameError;
    }

    public void setFullnameError(String fullnameError) {
        this.fullnameError = fullnameError;
    }

    public String getRoleError() {
        return roleError;
    }

    public void setRoleError(String roleError) {
        this.roleError = roleError;
    }
    
    
}
